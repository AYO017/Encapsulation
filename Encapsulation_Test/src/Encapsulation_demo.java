
public class Encapsulation_demo {

	private int age;
	private String firstName;
	private String secondName;

	// Getter and Setter methods

	public int getAge() {
		return age;
	}

	public String getfirstName() {
		return firstName;
	}

	public String getsecondName() {
		return secondName;
	}

	public void setAge(int From_User) {
		age = From_User;
	}

	public void setfirstName(String From_User) {
		firstName = From_User;
	}

	public void setsecondName(String From_User) {
		secondName = From_User;
	}
}
